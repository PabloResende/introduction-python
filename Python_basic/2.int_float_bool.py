#obs: type serve para perguntar o tipo

print(type('pablo'))#string is a word

print(type(10))#int são os números inteiros

print(type(1.2))#float é todo número com ponto flutuante

print(type(9 == 2))#bool é quando questiona se é verdadeiro ou falso, exemplo:
print(10 == 10)
#aqui o bool está questionando se 10 é igual a 10, no caso, verdadeiro(TRUE)
print((10 == 11))
#já nesse caso questiona se 10 e 11 são iguais, nesse caso é falso(FALSE)

